"""
28. Faça a leitura de três valores e apresente como resultado a soma dos quadrados dos
três valores lidos.
"""

valor_1 = float(input('Digite o primeiro valor: '))
valor_2 = float(input('Digite o segundo valor: '))
valor_3 = float(input('Digite o terceiro valor: '))
soma = ((valor_1 ** valor_1) + (valor_2 ** valor_2) + (valor_3 ** valor_3))
print(f'O resultado da soma dos quadrados é: {soma}')
