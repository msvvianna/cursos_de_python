print(7 * 2)  # multiplicação

print(4 - 2)  # subtração

print(5 / 2)  # divisão

print(5 // 2)  # divisão inteira

print(4 % 2)  # resto

print(5 % 2)  # resto

print(3 ** 3)  # potencia

